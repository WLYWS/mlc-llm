package ai.mlc.mlcchat.hl


data class AnswerResp(
    val statusCode: Int,
    val body: AnswersBody,
    val message: String? = null,
) {
    fun isOk() = statusCode == 200 && body.knowledge.isNotEmpty()
}

data class AnswersBody(
    val knowledge: List<String>,
)


